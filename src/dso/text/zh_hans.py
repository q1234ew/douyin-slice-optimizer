def to_zh_hans(value: object) -> str:
    """Normalize Chinese text to Simplified Chinese for display and downstream ASR features."""
    text = str(value or "")
    if not text:
        return ""
    converter = _opencc_converter()
    if converter is not None:
        try:
            return converter(text)
        except Exception:
            pass
    normalized = text
    for wrong, right in PHRASE_MAP.items():
        normalized = normalized.replace(wrong, right)
    return normalized.translate(CHAR_MAP)


@lru_cache(maxsize=1)
def _opencc_converter() -> Callable[[str], str] | None:
    try:
        from opencc import OpenCC  # type: ignore
    except Exception:
        return None
    try:
        converter = OpenCC("t2s")
    except Exception:
        try:
            converter = OpenCC("t2s.json")
        except Exception:
            return None
    return converter.convert
